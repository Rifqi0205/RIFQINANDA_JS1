let bulan = "Januari";

switch (bulan) {
  case "Januari":
    console.log("Ini adalah bulan pertama dalam tahun");
    break;
  case "Februari":
    console.log("Ini adalah bulan kedua dalam tahun");
    break;
  case "Maret":
    console.log("Ini adalah bulan ketiga dalam tahun");
    break;
  default:
    console.log("Ini bukan bulan Januari, Februari, atau Maret");
}
